#include <gct/format.hpp>

namespace gct {
  unsigned int format_to_component_size( vk::Format format ) {
    if( format == vk::Format::eR8Unorm) return 8u;
    else if( format == vk::Format::eR8Snorm) return 8u;
    else if( format == vk::Format::eR8Uscaled) return 8u;
    else if( format == vk::Format::eR8Sscaled) return 8u;
    else if( format == vk::Format::eR8Uint) return 8u;
    else if( format == vk::Format::eR8Sint) return 8u;
    else if( format == vk::Format::eR8Srgb) return 8u;
    else if( format == vk::Format::eR8G8Unorm) return 8u;
    else if( format == vk::Format::eR8G8Snorm) return 8u;
    else if( format == vk::Format::eR8G8Uscaled) return 8u;
    else if( format == vk::Format::eR8G8Sscaled) return 8u;
    else if( format == vk::Format::eR8G8Uint) return 8u;
    else if( format == vk::Format::eR8G8Sint) return 8u;
    else if( format == vk::Format::eR8G8Srgb) return 8u;
    else if( format == vk::Format::eR8G8B8Unorm) return 8u;
    else if( format == vk::Format::eR8G8B8Snorm) return 8u;
    else if( format == vk::Format::eR8G8B8Uscaled) return 8u;
    else if( format == vk::Format::eR8G8B8Sscaled) return 8u;
    else if( format == vk::Format::eR8G8B8Uint) return 8u;
    else if( format == vk::Format::eR8G8B8Sint) return 8u;
    else if( format == vk::Format::eR8G8B8Srgb) return 8u;
    else if( format == vk::Format::eB8G8R8Unorm) return 8u;
    else if( format == vk::Format::eB8G8R8Snorm) return 8u;
    else if( format == vk::Format::eB8G8R8Uscaled) return 8u;
    else if( format == vk::Format::eB8G8R8Sscaled) return 8u;
    else if( format == vk::Format::eB8G8R8Uint) return 8u;
    else if( format == vk::Format::eB8G8R8Sint) return 8u;
    else if( format == vk::Format::eB8G8R8Srgb) return 8u;
    else if( format == vk::Format::eR8G8B8A8Unorm) return 8u;
    else if( format == vk::Format::eR8G8B8A8Snorm) return 8u;
    else if( format == vk::Format::eR8G8B8A8Uscaled) return 8u;
    else if( format == vk::Format::eR8G8B8A8Sscaled) return 8u;
    else if( format == vk::Format::eR8G8B8A8Uint) return 8u;
    else if( format == vk::Format::eR8G8B8A8Sint) return 8u;
    else if( format == vk::Format::eR8G8B8A8Srgb) return 8u;
    else if( format == vk::Format::eB8G8R8A8Unorm) return 8u;
    else if( format == vk::Format::eB8G8R8A8Snorm) return 8u;
    else if( format == vk::Format::eB8G8R8A8Uscaled) return 8u;
    else if( format == vk::Format::eB8G8R8A8Sscaled) return 8u;
    else if( format == vk::Format::eB8G8R8A8Uint) return 8u;
    else if( format == vk::Format::eB8G8R8A8Sint) return 8u;
    else if( format == vk::Format::eB8G8R8A8Srgb) return 8u;
    else if( format == vk::Format::eA8B8G8R8UnormPack32) return 8u;
    else if( format == vk::Format::eA8B8G8R8SnormPack32) return 8u;
    else if( format == vk::Format::eA8B8G8R8UscaledPack32) return 8u;
    else if( format == vk::Format::eA8B8G8R8SscaledPack32) return 8u;
    else if( format == vk::Format::eA8B8G8R8UintPack32) return 8u;
    else if( format == vk::Format::eA8B8G8R8SintPack32) return 8u;
    else if( format == vk::Format::eA8B8G8R8SrgbPack32) return 8u;
    else if( format == vk::Format::eR16Unorm) return 16u;
    else if( format == vk::Format::eR16Snorm) return 16u;
    else if( format == vk::Format::eR16Uscaled) return 16u;
    else if( format == vk::Format::eR16Sscaled) return 16u;
    else if( format == vk::Format::eR16Uint) return 16u;
    else if( format == vk::Format::eR16Sint) return 16u;
    else if( format == vk::Format::eR16Sfloat) return 16u;
    else if( format == vk::Format::eR16G16Unorm) return 16u;
    else if( format == vk::Format::eR16G16Snorm) return 16u;
    else if( format == vk::Format::eR16G16Uscaled) return 16u;
    else if( format == vk::Format::eR16G16Sscaled) return 16u;
    else if( format == vk::Format::eR16G16Uint) return 16u;
    else if( format == vk::Format::eR16G16Sint) return 16u;
    else if( format == vk::Format::eR16G16Sfloat) return 16u;
    else if( format == vk::Format::eR16G16B16Unorm) return 16u;
    else if( format == vk::Format::eR16G16B16Snorm) return 16u;
    else if( format == vk::Format::eR16G16B16Uscaled) return 16u;
    else if( format == vk::Format::eR16G16B16Sscaled) return 16u;
    else if( format == vk::Format::eR16G16B16Uint) return 16u;
    else if( format == vk::Format::eR16G16B16Sint) return 16u;
    else if( format == vk::Format::eR16G16B16Sfloat) return 16u;
    else if( format == vk::Format::eR16G16B16A16Unorm) return 16u;
    else if( format == vk::Format::eR16G16B16A16Snorm) return 16u;
    else if( format == vk::Format::eR16G16B16A16Uscaled) return 16u;
    else if( format == vk::Format::eR16G16B16A16Sscaled) return 16u;
    else if( format == vk::Format::eR16G16B16A16Uint) return 16u;
    else if( format == vk::Format::eR16G16B16A16Sint) return 16u;
    else if( format == vk::Format::eR16G16B16A16Sfloat) return 16u;
    else if( format == vk::Format::eR32Uint) return 32u;
    else if( format == vk::Format::eR32Sint) return 32u;
    else if( format == vk::Format::eR32Sfloat) return 32u;
    else if( format == vk::Format::eR32G32Uint) return 32u;
    else if( format == vk::Format::eR32G32Sint) return 32u;
    else if( format == vk::Format::eR32G32Sfloat) return 32u;
    else if( format == vk::Format::eR32G32B32Uint) return 32u;
    else if( format == vk::Format::eR32G32B32Sint) return 32u;
    else if( format == vk::Format::eR32G32B32Sfloat) return 32u;
    else if( format == vk::Format::eR32G32B32A32Uint) return 32u;
    else if( format == vk::Format::eR32G32B32A32Sint) return 32u;
    else if( format == vk::Format::eR32G32B32A32Sfloat) return 32u;
    else if( format == vk::Format::eR64Uint) return 64u;
    else if( format == vk::Format::eR64Sint) return 64u;
    else if( format == vk::Format::eR64Sfloat) return 64u;
    else if( format == vk::Format::eR64G64Uint) return 64u;
    else if( format == vk::Format::eR64G64Sint) return 64u;
    else if( format == vk::Format::eR64G64Sfloat) return 64u;
    else if( format == vk::Format::eR64G64B64Uint) return 64u;
    else if( format == vk::Format::eR64G64B64Sint) return 64u;
    else if( format == vk::Format::eR64G64B64Sfloat) return 64u;
    else if( format == vk::Format::eR64G64B64A64Uint) return 64u;
    else if( format == vk::Format::eR64G64B64A64Sint) return 64u;
    else if( format == vk::Format::eR64G64B64A64Sfloat) return 64u;
    else if( format == vk::Format::eD16Unorm) return 16u;
    else if( format == vk::Format::eD32Sfloat) return 32u;
    else if( format == vk::Format::eS8Uint) return 8u;
    else return 0u;
  }
}

